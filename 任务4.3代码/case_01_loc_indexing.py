"""
案例1：使用loc进行标签索引
教材：《无人机遥感数据处理与分析》第四章 任务4.3
演示loc索引的基本用法：基于标签的索引操作
"""

import pandas as pd

# 创建无人机样点数据
data = {
    '样点ID': ['S001', 'S002', 'S003', 'S004', 'S005'],
    '区域': ['A', 'A', 'B', 'B', 'C'],
    'NDVI': [0.65, 0.72, 0.58, 0.81, 0.69],
    '温度': [25.3, 28.7, 23.9, 30.1, 26.5]
}
df = pd.DataFrame(data).set_index('样点ID')

# 使用loc选择数据
print("样点S003的数据:")
print(df.loc['S003'])

print("\n样点S001到S004（包含S004）:")
print(df.loc['S001':'S004'])

print("\n区域A和C的样点:")
print(df.loc[['S001', 'S002', 'S005']])